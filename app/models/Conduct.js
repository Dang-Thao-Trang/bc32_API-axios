function Users(
  id,
  userName,
  name,
  password,
  email,
  image,
  typeUser,
  typeLanguage,
  description
) {
  (this.id = id),
    (this.userName = userName),
    (this.name = name),
    (this.password = password),
    (this.email = email),
    (this.image = image),
    (this.typeUser = typeUser),
    (this.typeLanguage = typeLanguage),
    (this.description = description);
}
